#!/bin/bash

################################################################################
# 1. Delete all unnecessary files
################################################################################
sudo rm -rf /esaude/
sudo rm -rf /home/esaude/*
sudo umount /tmp/guest_additions
sudo rm -rf /tmp/*
sudo rm -rf /etc/puppet/modules

################################################################################
# 2. Install secure delete
################################################################################
sudo apt-get install -y secure-delete

################################################################################
# 3. Remove all unnecessary packages
################################################################################
sudo apt-get --purge remove -y puppet
sudo apt-get clean
sudo apt-get --purge autoremove -y

################################################################################
# 4. Remove MAC address binding
################################################################################
sudo rm -f /etc/udev/rules.d/70-persistent-net.rules

################################################################################
# 5. Fill empty space with zeros
################################################################################
sudo sfill -llz /
